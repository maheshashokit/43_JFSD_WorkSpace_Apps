
public class ForLoopDemo {

	public static void main(String[] args) {
		
		System.out.println("Started ::: For Loop Demo..");
		
		//printing the numbers from 10 to 1
		for(int loopStart = 10; loopStart > 0; loopStart--) {
			System.out.println("loopStart="+ loopStart);
		}
		
		System.out.println("Ended ::: For Loop Demo....");
	}

}
