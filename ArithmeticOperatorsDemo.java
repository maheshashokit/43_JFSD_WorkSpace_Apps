
public class ArithmeticOperatorsDemo {

	public static void main(String[] args) {
		
		int a = 10, b = 2;
		System.out.println("Addition Operations:::" + (a+b)); //12
		System.out.println("Subtraction Operations:::" + (a-b)); //8
		System.out.println("Multiplication Operations:::" + (a*b));//20
		System.out.println("Division Operations:::" + (a/b)); //5
		System.out.println("Modulo Operations:::" + (a%b)); //0
	}
}