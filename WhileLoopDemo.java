
public class WhileLoopDemo {
	
	public static void main(String[] args) {
		
		//loop variable need to be Initialized
		int loopStart=10;
		
		//writing the loop condition
		while(loopStart <= 20) {
			
			//loop statement
			System.out.println(loopStart + ". Welcome To AshokIT Hyderabad.....");
			
			//updating the counter variable
			loopStart = loopStart + 1;
		}
	}
}